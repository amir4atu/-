# 2. Функция сортировки по убыванию абсолютного значения
def list_sort(lst):
    return sorted(lst, key=lambda x: abs(x), reverse=True)


print("Сортировка по убыванию abs:", list_sort([3, -10, 5, -7, 2]))


# 3. Функция, меняющая местами первый и последний элементы
def change(lst):
    lst[0], lst[-1] = lst[-1], lst[0]
    return lst


print("После обмена:", change([10, 20, 30, 40]))