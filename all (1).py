# сложение
x = 7 + 3
print("7 + 3 =", x)

# вычитание
x = 10 - 4
print("10 - 4 =", x)

# умножение
x = 6 * 5
print("6 * 5 =", x)

# возведение в степень
x = 2 ** 4
print("2 ** 4 =", x)

# деление
x = 15 / 2
print("15 / 2 =", x)

# целочисленное деление
x = 15 // 2
print("15 // 2 =", x)

# остаток от деления
x = 15 % 2
print("15 % 2 =", x)